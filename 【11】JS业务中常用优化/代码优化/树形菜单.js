const detailMenus = []
function getMenus(treeMenus, parent, loopNumArg) {
  let loopNum = 1
  if (loopNumArg) {
    loopNum = loopNumArg
    loopNum++
  }

  for (let i = 0; i < treeMenus.length; i++) {
    const menus = treeMenus[i]

    let parentName = ''
    const key = `level_${loopNum}`
    const href = `level_${loopNum}_href`
    if (parent) {
      menus[key] = parent.name
      menus[href] = parent.href
      parentName = parent.name
    }
    console.log(`${menus[key]}>> ${parentName}>>${menus.name}：${menus.id}`)
    if (menus.name === '查看') {
      // const DetailKey = ` ${parentName}--${menus.name}-detail`
      let detailKey = ''
      if (parent.level_1) {
        detailKey += parent.level_1
      }
      if (parent.level_2) {
        detailKey += parent.level_2
      }
      if (parent.level_3) {
        detailKey += parent.level_3
      }

      detailKey += `--${parentName}--${menus.name}-detail`
      const detailMenu = {}
      detailMenu[detailKey] = menus.id
      detailMenu.href = parent.href
      detailMenus.push(detailMenu)
    }
    if (menus.children) {
      getMenus(menus.children, menus, loopNum)
    }
  }
}

